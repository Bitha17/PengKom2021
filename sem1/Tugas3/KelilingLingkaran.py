# Program KelilingLingkaran
# Spesifikasi Program : Program ini menerima input berupa panjang jari-jari lingkaran dan memberi output berupa keliling lingkaran

# KAMUS
# x : int
# keliling : float

# ALGORITMA
# Menerima besar jari-jari lingkaran
x = int(input("Panjang jari-jari: "))
# Hitung keliling lingkaran
keliling = 2*x*3.14
# Memberikan keliling lingkaran
print(f"Keliling lingkaran: {keliling} satuan")